    public void RemplirLesEditions(){
        racineEdition.removeAllChildren();
        for (String pigiste : mesEditions.keySet()){
            noeudPigiste = new DefaultMutableTreeNode(pigiste);
            for (String magazine : mesEditions.get(pigiste).keySet()){
                noeudMagazines = new DefaultMutableTreeNode(magazine);
                for (Article unArticle : mesEditions.get(pigiste).get(magazine)){
                    noeudNomArticles = new DefaultMutableTreeNode(unArticle.getTitreArticle());
                    noeudMagazines.add(noeudNomArticles);
                    noeudNomArticles = new DefaultMutableTreeNode(unArticle.getNbFeuillets());
                    noeudMagazines.add(noeudNomArticles);
                }
                noeudPigiste.add(noeudMagazines);
            }
            racineEdition.add(noeudPigiste);
        }
        modelEdition = new DefaultTreeModel(racineEdition);
        trEditions.setModel(modelEdition);
        
        for(int i = 0 ; i < trEditions.getRowCount() ; i++)
        {
            trEditions.expandRow(i);
            
           
        }
        
    }
    
